export class FoodProduct {

    id: Number
    calories: Number
    carbohydrates: Number
    label: String
    barcode: Number


    constructor(id: Number, calories: Number, carbohydrates: Number, label: String, barcode: Number) {
        this.id = id;
        this.calories = calories;
        this.carbohydrates = carbohydrates;
        this.label = label;
        this.barcode = barcode;
    }



}
